                                <p align="justify">
                                    Os dirigentes da FOJEBRA estiveram em reunião na terça-feira (13/03), 
                                    na parte da tarde, com a Dra. Silvana Borges, Diretora Executiva do DIREX, 
                                    órgão auxiliar da Polícia Federal, e com o Dr. Tony Gean de Castro, Chefe da 
                                    Divisão Nacional de Armas (DARM).
                                </p>
                                <p align="justify">
                                    O presidente da FOJEBRA, Edvaldo Lima, abriu a reunião apresentando a Federação, 
                                    seus estados componentes, e fazendo um breve histórico do tema “Porte de Arma para 
                                    os Oficiais de Justiça”.
                                </p>
                                <p align="justify">
                                    Oficiais de Justiça de 14 estados do Brasil estiveram presentes e expuseram as 
                                    dificuldades específicas encontradas em suas regiões no cumprimento das ordens 
                                    judicias. Os diretores da Polícia Federal afirmaram que a instituição está ciente 
                                    dessas dificuldades e salientaram que medidas já estão sendo adotadas pelo órgão, 
                                    inclusive o Direx já enviou ofício às superintendências regionais tratando do assunto.
                                </p>
                                <p align="justify">
                                    Segundo a Dra. Silvana, a atividade dos Oficiais de Justiça “é uma das tarefas mais 
                                    difíceis do Poder Judiciário e entendo que o pedido é justo pela natureza do trabalho 
                                    que eu conheço muito bem”. A  delegada acrescentou também que o Diretor Geral da PF, 
                                    Dr. Rogério Augusto Viana Galloro, também é sensível ao pedido da categoria.
                                </p>
                                <p align="justify">
                                    Já o Chefe da Divisão Nacional de Armas, Dr. Tony, elogiou o dossiê elaborado pela 
                                    Federação sobre crimes cometidos contra Oficiais de Justiça e afirmou que o 
                                    PLC 30/2007 ainda é o melhor caminho para a conquista do pleito pela categoria, 
                                    porque a Polícia Federal não pode suprir o papel do legislador, mas fez o que estava 
                                    ao alcance da instituição lançando o Ofício Circular 05/2017 através da DIREX.
                                </p>
                                <p align="justify">
                                    Foi discutida, ainda, a possibilidade de alteração na Instrução Normativa 23/2005, 
                                    com o intuito de deixar o texto mais favorável a essa pretensão dos Oficiais de 
                                    Justiça, o que acabou parecendo a todos uma boa solução.
                                </p>
                                <p align="justify">
                                    Aproveitando o gancho, o Diretor de Assuntos Legislativos da FOJEBRA, Joselito 
                                    Bandeira Vicente, ressaltou o alto índice de violência contra Oficiais de Justiça 
                                    no Brasil. “Desde o Estatuto do Desarmamento, tivemos 31 Oficiais mortos, um número 
                                    superior até às mortes de Policiais Federais, que foram 10”, disse o dirigente.
                                </p>
                                <p align="justify">
                                    Demais presidentes de entidades fizeram suas colocações complementando as falas e 
                                    enriquecendo o debate.
                                </p>
                                <p align="justify">
                                    Na oportunidade, o Presidente da FOJEBRA, Edvaldo Lima, agradeceu a receptividade, 
                                    o empenho da PF, e se prontificou a ajudar no que for preciso.
